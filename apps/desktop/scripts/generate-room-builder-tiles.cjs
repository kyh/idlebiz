// Slice the Room Builder Office tileset (1_Room_Builder_Office) into individual
// 32px tiles + a generated catalog, so the builder can paint floors/walls/structure.
const sharp = require("sharp");
const fs = require("fs");
const path = require("node:path");

const SRC =
  "/Users/kyh/Desktop/vg/office/Modern_Office_Revamped_v1.2/1_Room_Builder_Office/Room_Builder_Office_32x32.png";
const TILE = 32;
const appRoot = path.resolve(__dirname, "..");
const outDir = path.join(appRoot, "public/workspace-kit/room-builder/32");
const catalogPath = path.join(appRoot, "src/renderer/game/room-builder-tiles.generated.ts");

(async () => {
  const img = await sharp(SRC).ensureAlpha().raw().toBuffer({ resolveWithObject: true });
  const W = img.info.width,
    H = img.info.height,
    D = img.data;
  const cols = Math.floor(W / TILE),
    rows = Math.floor(H / TILE);
  fs.rmSync(outDir, { recursive: true, force: true });
  fs.mkdirSync(outDir, { recursive: true });
  const tiles = [];
  for (let r = 0; r < rows; r++) {
    for (let c = 0; c < cols; c++) {
      const buf = Buffer.alloc(TILE * TILE * 4);
      let opaque = 0;
      for (let y = 0; y < TILE; y++) {
        for (let x = 0; x < TILE; x++) {
          const so = ((r * TILE + y) * W + (c * TILE + x)) * 4;
          const po = (y * TILE + x) * 4;
          buf[po] = D[so];
          buf[po + 1] = D[so + 1];
          buf[po + 2] = D[so + 2];
          buf[po + 3] = D[so + 3];
          if (D[so + 3] > 20) opaque++;
        }
      }
      if (opaque < 8) continue; // skip blank tiles
      const name = `tile-${c}-${r}.png`;
      await sharp(buf, { raw: { width: TILE, height: TILE, channels: 4 } })
        .png()
        .toFile(path.join(outDir, name));
      tiles.push({
        id: `rb-${c}-${r}`,
        col: c,
        row: r,
        path: `workspace-kit/room-builder/32/${name}`,
      });
    }
  }
  const body = tiles
    .map((t) => `  { id: "${t.id}", col: ${t.col}, row: ${t.row}, path: "${t.path}" },`)
    .join("\n");
  const ts = `// Generated by scripts/generate-room-builder-tiles.cjs — do not edit.\n// ${tiles.length} tiles sliced from Room_Builder_Office_32x32.png (${cols}x${rows} grid).\nexport interface RoomBuilderTile {\n  readonly id: string;\n  readonly col: number;\n  readonly row: number;\n  readonly path: string;\n}\nexport const ROOM_BUILDER_TILES: readonly RoomBuilderTile[] = [\n${body}\n];\n`;
  fs.writeFileSync(catalogPath, ts);
  console.log(
    `sliced ${tiles.length} tiles from ${cols}x${rows} grid -> ${outDir} + room-builder-tiles.generated.ts`,
  );
})();
