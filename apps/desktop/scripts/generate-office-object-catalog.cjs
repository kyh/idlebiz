const fs = require("node:fs");
const path = require("node:path");
const sharp = require("sharp");
const { loadRaw, opaqueBounds } = require("./lib/pixels.cjs");

const repoRoot = path.resolve(__dirname, "../../..");
const defaultSource =
  "/Users/kyh/Desktop/vg/office/Modern_Office_Revamped_v1.2/4_Modern_Office_singles";
const sourceDir = process.env.OFFICE_OBJECT_SOURCE || defaultSource;
const publicDir = path.join(repoRoot, "apps/desktop/public/workspace-kit/office-objects");
const generatedPath = path.join(
  repoRoot,
  "apps/desktop/src/renderer/game/office-object-catalog.generated.ts",
);

const scales = [32]; // the game renders at 2x only — 16/48 variants were dead weight

function fail(message) {
  console.error(message);
  process.exit(1);
}

function sourceForScale(scale) {
  return path.join(sourceDir, `${scale}x${scale}`);
}

function fileId(file) {
  const match = /_(\d+)\.png$/.exec(file);
  return match ? Number(match[1]) : null;
}

function targetFile(scale, id) {
  return `modern-office-${scale}-${String(id).padStart(3, "0")}.png`;
}

async function copyImage(sourcePath, targetPath) {
  const bounds = opaqueBounds(await loadRaw(sourcePath));
  const metadata = await sharp(sourcePath).metadata();
  if (!metadata.width || !metadata.height) fail(`Missing image size: ${sourcePath}`);
  await sharp(sourcePath).png().toFile(targetPath);
  return { w: metadata.width, h: metadata.height, bounds };
}

function tsString(value) {
  return JSON.stringify(value);
}

function variantBlock(variant) {
  return `      {
        scale: ${variant.scale},
        path: ${tsString(variant.path)},
        w: ${variant.w},
        h: ${variant.h},
        bounds: { x: ${variant.bounds.x}, y: ${variant.bounds.y}, w: ${variant.bounds.w}, h: ${variant.bounds.h} },
      },`;
}

function objectBlock(object) {
  return `  {
    id: ${tsString(object.id)},
    sourceId: ${object.sourceId},
    variants: [
${object.variants.map(variantBlock).join("\n")}
    ],
  },`;
}

function writeGenerated(objects) {
  const contents = `// Generated by apps/desktop/scripts/generate-office-object-catalog.cjs.

export interface OfficeObjectVariant {
  readonly scale: 16 | 32 | 48;
  readonly path: string;
  readonly w: number;
  readonly h: number;
  readonly bounds: {
    readonly x: number;
    readonly y: number;
    readonly w: number;
    readonly h: number;
  };
}

export interface OfficeObjectAsset {
  readonly id: string;
  readonly sourceId: number;
  readonly variants: readonly OfficeObjectVariant[];
}

export const OFFICE_OBJECT_ASSETS: readonly OfficeObjectAsset[] = [
${objects.map(objectBlock).join("\n")}
];
`;
  fs.writeFileSync(generatedPath, contents);
}

async function main() {
  if (!fs.existsSync(sourceDir)) {
    fail(`Missing source: ${sourceDir}\nSet OFFICE_OBJECT_SOURCE=/path/to/4_Modern_Office_singles`);
  }

  fs.rmSync(publicDir, { recursive: true, force: true });
  for (const scale of scales) fs.mkdirSync(path.join(publicDir, `${scale}`), { recursive: true });

  const ids = new Set();
  for (const scale of scales) {
    const dir = sourceForScale(scale);
    if (!fs.existsSync(dir)) fail(`Missing scale folder: ${dir}`);
    for (const file of fs.readdirSync(dir)) {
      if (!file.endsWith(".png")) continue;
      const id = fileId(file);
      if (id !== null) ids.add(id);
    }
  }

  const objects = [];
  for (const sourceId of Array.from(ids).toSorted((a, b) => a - b)) {
    const variants = [];
    for (const scale of scales) {
      const dir = sourceForScale(scale);
      const prefix =
        scale === 16 ? "Modern_Office_Singles_" : `Modern_Office_Singles_${scale}x${scale}_`;
      const sourcePath = path.join(dir, `${prefix}${sourceId}.png`);
      if (!fs.existsSync(sourcePath)) continue;
      const file = targetFile(scale, sourceId);
      const targetPath = path.join(publicDir, `${scale}`, file);
      const size = await copyImage(sourcePath, targetPath);
      variants.push({
        scale,
        path: `workspace-kit/office-objects/${scale}/${file}`,
        w: size.w,
        h: size.h,
        bounds: size.bounds,
      });
    }
    if (variants.length > 0) {
      objects.push({
        id: `office-object-${String(sourceId).padStart(3, "0")}`,
        sourceId,
        variants,
      });
    }
  }

  writeGenerated(objects);
  console.log(`generated ${objects.length} office object assets`);
}

void main();
